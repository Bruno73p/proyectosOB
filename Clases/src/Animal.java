public abstract class Animal {
    int patas;
    String raza;
    String tipoDePelo;
    String colorDePelo;
    boolean cola;

    public Animal(int patas, String raza, String tipoDePelo, String colorDePelo, boolean cola) {
        this.patas = patas;
        this.raza = raza;
        this.tipoDePelo = tipoDePelo;
        this.colorDePelo = colorDePelo;
        this.cola = cola;

    }
}
